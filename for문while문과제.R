5.  0, 1, 1, 2, 3, 5, 8, 13, 21......의 형태의 수열.
즉, 첫 번째 항의 값은 0이고 두 번째 항의 값은 1일 때 이후의 항들은 이전의 두 항을 더한 값으로 만들어지는 
수열을 피보나치 수열이라고 한다. 0 에서 부터 시작하여 40개의 피보나치 수열을 출력하시오.



n <- 0
fibo <-c(0,1)

while(n<=40){
  tmp <- sum(tail(fibo,2))
  fibo <- append(fibo,tmp)
  n <-n+1
}
print(fibo)
