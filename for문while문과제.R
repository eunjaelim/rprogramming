# 1. 1~100 사이의 정수 중 3의 배수들의 합과 개수를 구하시오. 
sum = 0
for (i in 1:100){
  if (i %% 3 == 0){
    i = i+1
    sum = sum + i
    print(i)
    print(sum)
  } 
}

